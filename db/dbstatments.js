export default function dbInit() {
    return `

      CREATE TABLE IF NOT EXISTS workouts (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT,
        start_time TEXT,
        end_time TEXT
        );
      
      CREATE TABLE IF NOT EXISTS exercises (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        name TEXT
        );

      CREATE TABLE IF NOT EXISTS sets (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        reps INTEGER,
        weight INTEGER,
        exercise_id INTEGER,
        workout_id INTEGER,
        FOREIGN KEY (exercise_id) REFERENCES exercises(id) ON DELETE CASCADE,
        FOREIGN KEY (workout_id) REFERENCES workouts(id) ON DELETE CASCADE
        );


      CREATE TABLE IF NOT EXISTS weight (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        body_weight REAL,
        unit_of_measure TEXT,
        date TEXT
        );

      CREATE TABLE IF NOT EXISTS foods (
        id INTEGER PRIMARY KEY,
        name TEXT NOT NULL,
        calories_per_serving REAL,
        serving_size TEXT,
        num_servings REAL DEFAULT 1,
        protein_per_serving REAL,
        carbs_per_serving REAL,
        fat_per_serving REAL,
        total_calories REAL AS (calories_per_serving * num_servings) STORED,
        total_protein REAL AS (protein_per_serving * num_servings) STORED,
        total_carbs REAL AS (carbs_per_serving * num_servings) STORED,
        total_fat REAL AS (fat_per_serving * num_servings) STORED
      );  

      CREATE TABLE IF NOT EXISTS food_logs (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        log_date TEXT NOT NULL UNIQUE,  
        notes TEXT
      );

      CREATE TABLE IF NOT EXISTS log_food_entries (
        log_id INTEGER NOT NULL,
        food_id INTEGER NOT NULL,
        PRIMARY KEY (log_id, food_id),  -- Prevent duplicate food entries per log
        FOREIGN KEY (log_id) REFERENCES daily_logs(id) ON DELETE CASCADE,
        FOREIGN KEY (food_id) REFERENCES foods(id) ON DELETE CASCADE
      );


    
    
      `
}

